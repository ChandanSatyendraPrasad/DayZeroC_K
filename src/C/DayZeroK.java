package C;

public class DayZeroK {
    public static void main(String[] args) {
        int a=35,b=90;
        float aa=a,bb=b;
        double aaa=aa+a,bbb=bb+b;
        System.out.println(aaa + " "+bbb);
        System.out.println("type conversion="+aa+" and "+bb);
        char abc = (char) bbb;
        System.out.println("Type Casting :: "+abc);
    }
}
